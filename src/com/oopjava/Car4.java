package com.oopjava;

public class Car4 {

	public String make,model;
	public int year;
	public Car4(String make, String model, int year) {
		super();
		this.make = make;
		this.model = model;
		this.year = year;
	}
	@Override
	public String toString() {
		return "Car4 [make=" + make + ", model=" + model + ", year=" + year + "]";
	}
	
	
}
